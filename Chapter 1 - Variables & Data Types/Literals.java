// Literals in Java.

// Literals are the constant value assign to a variable. The different types of literals are as follows.

// 1) Integer Literal
// 2) Float Literal
// 3) Double Literal
// 4) Character Literal 
// 5) Boolean Literal
// 6) String Literal

public class Literals {
    public static void main(String[] args) {
        
        int a = 5; // Integer Literal
        float b = 5.0f; // Float Literal
        double c = 5.0; // Double Literal
        char d = 'A'; // Character Literal
        boolean flag = true; // Boolean Literal
        String name = "Harry"; // String Literal


        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(flag);
        System.out.println(name);

    }
}
