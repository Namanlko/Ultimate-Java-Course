// Question 10: A Do While loop is executed.

// 1) At least once.
// 2) At least twice.
// 3) At most once.

// Answer - At least once.

public class Question10 {
    public static void main(String[] args) {
        do{
            System.out.println("Hello Do While!");
        }
        while(5<3);
    }
}
