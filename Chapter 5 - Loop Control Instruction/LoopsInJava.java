// Loops in Java.

// Loops in java are used to execute a line of code for multiple number of time util a condition is met.

// There are three types of loops in java.
// 1) While Loop
// 2) Do While Loop
// 3) For Loop

public class LoopsInJava{
    public static void main(String[] args) {
        // Let's understand for loop with the help of an example.

        for(int i=1; i<=5; i++){ // Here loop is repeatedly printing the value of i from 1 to 5 till i become 5.
            System.out.println(i);
        }

    }
}