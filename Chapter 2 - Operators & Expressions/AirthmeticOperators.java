// Airthmetic Operators in Java.

// Airthmetic operators are used to perform airthmetic operations in Java like addition, subtraction, multiplication and division.

public class AirthmeticOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        
        // 1) + Operator:
        int addition = a + b;
        System.out.println(addition);

        // 2) - Operator:
        int subtraction = a - b;
        System.out.println(subtraction);

        // 3) * Operator:
        int multiplication = a * b;
        System.out.println(multiplication);

        // 4) / Operator:
        int division = a / b;
        System.out.println(division);

        // 5) % (Remainder) Operator:
        int remainder = a % b;
        System.out.println(remainder);
        
    }
}
