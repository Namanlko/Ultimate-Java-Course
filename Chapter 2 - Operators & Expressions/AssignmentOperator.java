// Assignment Operator.

// Assignment operator is used to assign some value to a variable.

public class AssignmentOperator {
    public static void main(String[] args) {
        
        // Let's understand with the help of an example.
        int a = 10; // Here variable a is assigned with the value 10 using assignment operator.
        int b = 20; // Here variable b is assigned with the value 20 using assignment operator.
        int sum = a + b; // Here variable sum is assigned with the expression of (a + b) using assignment operator.

        System.out.println("Value of a is "+a);
        System.out.println("Value of b is "+b);
        System.out.println("Value of sum is "+sum);

    }
}
