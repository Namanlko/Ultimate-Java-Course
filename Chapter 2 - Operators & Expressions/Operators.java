// Operators in Java.

// Operators are used to perform operations on variables and values.
// There are five differenet types of operators used in Java.
// 1) Airthmetic Operator.
// 2) Assignment Operator.
// 3) Comparison Operator.
// 4) Logical Operator.
// 5) Bitwise Operator.

public class Operators{
    public static void main(String[] args) {
        
        //Let's understand with the help of an example.

        int a = 10; // Variable a assign with the value 10 using assignment operator.
        int b = 15; // Variable b assign with the value 15 using assignment operator.
        int sum = a + b; // Here we are doing some of a & b using + arithmetic operator and storing it in third variable named sum.
        System.out.println("Sum = "+sum); // Here we are displaying value of sum at output console.

    }
}