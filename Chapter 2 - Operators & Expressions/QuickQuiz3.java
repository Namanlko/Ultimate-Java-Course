// Quick Quiz 3: What will be the value of the following expression(x)

// int y = 7;
// int x = ++y*8;
// Value of x?

// char a = 'B';
// a++;
// Value of a?

public class QuickQuiz3 {
    public static void main(String[] args) {
        
        int y = 7;
        int x = ++y*8;
        System.out.println("Value of x is "+x);

        char a = 'B';
        a++;
        System.out.println("Value of a is "+a);
    }
}
