// Comparsion operators in Java.

// Comparsion operator is used to compare two different values in Java and based on the it return output in boolean (true or false).

public class ComparisonOperators {
    public static void main(String[] args) {
        
        int x = 5;
        int y = 3;

        System.out.println(x == y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);

    }
}
