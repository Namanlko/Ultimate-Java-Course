// Escape Sequence Characters.

// Escape Sequence characters consist of more than one characters but represents one character when used within the Strings.

public class EscapeSequence {
    public static void main(String[] args) {
        
        System.out.println("Hello,\n\tThis is Java Course!"); // Here \n is used to move the cursor to nextline and and \t is used to give tab space.
        
    }
}
